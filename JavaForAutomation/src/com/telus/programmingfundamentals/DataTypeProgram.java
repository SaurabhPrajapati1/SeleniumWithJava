package com.telus.programmingfundamentals;

public class DataTypeProgram {

	public static void main(String[] args) {
		
		// byte data type:-
		
		byte month = 9;
		System.out.println(month);
		System.out.println("The Month number is "+ month);
		
		// short data type:-
		
		short quantity = 2000;
		System.out.println("The short data type quantity is "+ quantity);
		
		// integer data type:-
		int rollNumber = 12345;
		System.out.println("My Roll Number is "+ rollNumber);
		
		// long data type- 'L' needs to be added to the number
		long profit = 123456789986765L;
		System.out.println("The long data type value is "+ profit);
		
		// float data type-'f' needs to be added to the number
		float pieValue = 3.141592653589793238f;
		System.out.println("The float data type value is "+ pieValue);
		
		// float data type - type case float
		float pieValue2 = (float) 3.141592653589793238;
		System.out.println(" The float data type value is "+ pieValue2);
		
		// double data type:-
		double pieValue3 = 3.141592653589793238;
		System.out.println("The double data type value is "+ pieValue3);
		
		// boolean data type value:-
		boolean myBoolean = true;
		System.out.println(myBoolean);
		System.out.println("My Boolean Value is "+ myBoolean);
		
		// character data type:-
		char myLetter = 'R';
		System.out.println("My character value is "+ myLetter);
		
		// String data type:-
		String firstName = "Raj";
		String lastName = "Kumar";
		System.out.println(firstName + lastName);
		System.out.println("My Name is "+ firstName + lastName);
		
		
		
		
		
		


	}

}
